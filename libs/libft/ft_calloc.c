#include "libft.h"

void	*ft_calloc(size_t num, size_t size)
{
	void	*arr;

	if (num * size == 0)
		return (malloc(0));
	if (!(arr = (void *)malloc(size * num)))
		return (0);
	ft_bzero(arr, size * num);
	return (arr);
}
