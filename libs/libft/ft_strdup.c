#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	int		len;
	char	*arr;

	len = ft_strlen(s);
	if (!(arr = (char *)malloc(sizeof(char) * (len + 1))))
		return (0);
	i = 0;
	while (i < len)
	{
		arr[i] = s[i];
		i++;
	}
	arr[i] = 0;
	return (arr);
}
